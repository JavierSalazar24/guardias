import { useMutation, useQuery, useQueryClient } from '@tanstack/react-query'
import { useModalStore } from '../store/useModalStore'
import Swal from 'sweetalert2'
import {
  createLimpiezaProgramada,
  getLimpiezaProgramada,
  removeLimpiezaProgramada,
  updateLimpiezaProgramada
} from '../api/limpiezas-programadas'
import { toast } from 'sonner'

export const useLimpiezasProgramadas = () => {
  // Store de modal
  const modalType = useModalStore((state) => state.modalType)
  const formData = useModalStore((state) => state.formData)
  const closeModal = useModalStore((state) => state.closeModal)

  const queryClient = useQueryClient()

  const { isLoading, isError, data, error } = useQuery({
    queryKey: ['limpiezas-programadas'],
    queryFn: getLimpiezaProgramada
  })

  const createMutation = useMutation({
    mutationFn: createLimpiezaProgramada,
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['limpiezas-programadas'] })
      toast.success('Registro agregado')
      Swal.close()
      closeModal()
    },
    onError: (error) => {
      Swal.close()
      toast.error(error.message)
    }
  })

  const updateMutation = useMutation({
    mutationFn: updateLimpiezaProgramada,
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['limpiezas-programadas'] })
      toast.success('Registro actualizado')
      closeModal()
      Swal.close()
    },
    onError: (error) => {
      toast.error(error.message)
      Swal.close()
    }
  })

  const deleteMutation = useMutation({
    mutationFn: removeLimpiezaProgramada,
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['limpiezas-programadas'] })
      toast.success('Registro eliminado')
    },
    onError: (error) => {
      toast.error(error.message)
    }
  })

  const handleSubmit = (e) => {
    e.preventDefault()
    Swal.fire({
      title:
        '<h2 style="font-family: "sans-serif";">Guardando registro, por favor espere...</h2>',
      allowEscapeKey: false,
      allowOutsideClick: false,
      timerProgressBar: true,
      didOpen: () => {
        Swal.showLoading()
      }
    })

    if (modalType === 'add') {
      createMutation.mutate(formData)
    } else if (modalType === 'edit') {
      updateMutation.mutate(formData)
    }
  }

  const handleDelete = (id) => {
    deleteMutation.mutate(id)
    closeModal()
  }

  return {
    data,
    error,
    isError,
    isLoading,
    handleSubmit,
    handleDelete
  }
}
